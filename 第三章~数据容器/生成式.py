# python中生成式、推导式语法结构相同，主要区别在于返回的数据类型和内存使用方式。
# 语法结构相似：都采用表达式 for 变量 in 可迭代对象 if 条件的结构形式
# 使用方式一致：都可以添加if条件筛选，都支持类似的嵌套语法
# 关键区别：生成器表达式是“懒加载”的，节省内存；列表推导式立即生成所有数据，占用内存；

# 在Python生成式中，for 前面的表达式（参数）定义了生成式中每个元素的值。这个表达式可以是一个简单的变量、一个复杂的表达式、
# 一个函数调用等，具体取决于生成式的需要。具体取决于生成式的需要。
# 列表生成式：表达式决定新列表中每个元素的值。例如[x*x for x in range(5)]中，
# x*x是表达式，生成[0,1,4,9,16]1。
# 字典生成式：表达式分为键和值两部分，决定新字典的结构。例如{x: x*2 for x in range(3)}中，
# x是键表达式，x*2是值表达式，生成{0:0,1:2,2:4}。
# 集合生成式：表达式决定新集合中每个元素的值。例如{x%3 for x in range(5)}中，x%3是表达式，生成{0,1,2}。
# 该表达式通常包含循环变量（如x），可进行运算或条件筛选（如x if x%2==0 else -x）。
# 其核心作用是将遍历过程与元素转换结合，直接生成目标容器

# 生成式（即生成器表达式，（用于列表/字典/集合推导式，元组返回的是一个生成器对象，需要转换成元组类型）和"推导式"（用于列表/字典/集合推导式，不直接用于元组）是用于创建数据结构的简洁语法，
# 而迭代器是一种可遍历的对象，生成器表达式返回的是迭代器，但列表推导式返回的是列表，不是迭代器。
# 生成式（生成器表达式）：用圆括号定义，例如 (x*x for x in range(10))，返回一个生成器对象（一种迭代器），
# 几乎不占内存，被迭代时才计算值。
# 推导式：包括列表推导式（用[]，如[x*x for x in range(10)]，返回列表）、字典推导式（用{}，构建键值对）
# 和集合推导式（用{}，生成集合），返回的是具体的数据结构，不是迭代器。
# 迭代器：是一个可遍历的对象，能记住遍历位置，通过iter()和next()方法访问元素，只能往前不会后退，
# 例如通过iter([1,2,3])创建的迭代器。
# 迭代器是访问集合元素的一种方式，所有可作用于for循环的对象都是可迭代对象，但不是所有可迭代对象都是迭代器（如列表需要通过iter()转换）